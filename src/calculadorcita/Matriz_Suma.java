    /**
     * Crea matriz en la interfaz
     */
    private void createMatrizA(JPanel container, CalculadoraDeMatrices matriz) {
        //bordes y layout de contenedor
        container.setBorder(BorderFactory.createMatteBorder(0, 2, 0, 2, new Color(0, 0, 0)));
        container.setLayout(new java.awt.GridLayout(this.dimensionx, this.dimensiony));

        JCTextField[][] m = matriz.getMatriz();
        for (int fila = 0; fila < this.dimensionx; fila++) {
            for (int columna = 0; columna < this.dimensiony; columna++) {
                container.add(m[fila][columna]);
            }
        }
        container.setSize(4 + this.dimensionx * 36, this.dimensiony * 36);
        container.setPreferredSize(new Dimension(4 + this.dimensionx * 36, this.dimensiony * 36));
        container.repaint();
    }

